// Implicit conversion - Coercion

//console.log(1 + 2);
console.log('1' + 2); // 2 got converted to a String - '1' + '2'

console.log('1' * 2); // 1 got converted to a Number - 1 * 2



// Explicit conversion - Type Casting

console.log(Number('2')); // '2' gets converted to a Number - 2

console.log(String(2));   // 2 gets converted to a String -  '2'